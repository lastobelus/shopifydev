 def self.valid_fsa
   province = ValidPostalCode::PROVINCES.keys.sample
   fsa = ValidPostalCode::PROVINCES[province].keys.sample
   city = ValidPostalCode::PROVINCES[province][fsa]
   {province: province, fsa: fsa, city: city}
 end
 def self.valid_place
   place = valid_fsa
   place[:postal_code] = place.delete(:fsa) + " " + ::FFaker.bothify('#?#').upcase
   place
 end
